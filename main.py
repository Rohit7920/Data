def _search_single(query: str, top_k: int) -> list:
    """Run one FAISS query and return top-k metadata dicts."""
    emb = embedding_model.encode([query], batch_size=1)
    emb = np.array(emb, dtype=np.float32)
    faiss.normalize_L2(emb)
    scores, indices = index.search(emb, min(top_k, len(documents)))
    results = []
    for score, idx in zip(scores[0], indices[0]):
        if idx >= 0 and float(score) >= 0.10:
            results.append(documents[idx]["metadata"])
    return results


def retrieve_assessments(messages: list, top_k: int = 10) -> list:
    user_text = " ".join(
        m["content"] for m in messages if m["role"] == "user"
    )

    if not user_text.strip():
        return [doc["metadata"] for doc in documents[:top_k]]

    prev_rec_names = []
    for m in messages:
        if m["role"] == "assistant":
            for item in catalog:
                if item["name"].lower() in m["content"].lower():
                    prev_rec_names.append(item["name"])

    if prev_rec_names:
        user_text = user_text + " " + " ".join(prev_rec_names[:5])

    all_queries = [user_text] + SOFT_EXPANSIONS

    seen_ids = set()
    merged   = []

    for q in all_queries:
        batch = _search_single(q, top_k=top_k)
        for item in batch:
            eid = item.get("entity_id")
            if eid not in seen_ids:
                seen_ids.add(eid)
                merged.append(item)
        if len(merged) >= top_k * 2:
            break

    return merged[:top_k]

