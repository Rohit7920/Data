def classify_intent_llm(messages: list) -> str:

    latest_user = ""

    for m in reversed(messages):
        if m["role"] == "user":
            latest_user = m["content"].lower()
            break

    role_keywords = [
        "developer", "engineer", "manager", "analyst",
        "sales", "java", "python", "cloud", "data",
        "role", "hiring", "assessment", "sql",
        "stakeholder", "leadership", "communication"
    ]

    compare_keywords = [
        "compare", "difference", "vs", "versus"
    ]

    refine_keywords = [
        "add", "remove", "include", "also", "instead"
    ]

    offtopic_keywords = [
        "salary", "politics", "legal", "law"
    ]

    if any(k in latest_user for k in compare_keywords):
        return "compare"

    if any(k in latest_user for k in refine_keywords):
        return "refine"

    if any(k in latest_user for k in offtopic_keywords):
        return "refuse"

    if any(k in latest_user for k in role_keywords):
        return "recommend"

    conversation_text = "\n".join(
        f"{m['role'].upper()}: {m['content']}"
        for m in messages
    )

    prompt = f"""
Classify the latest user intent.

Return ONLY one word:
clarify
recommend
refine
compare
refuse

Conversation:
{conversation_text}
"""

    try:

        resp = groq_client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=[{
                "role": "user",
                "content": prompt
            }],
            temperature=0,
            max_tokens=10
        )

        intent = resp.choices[0].message.content.strip().lower()

        allowed = [
            "clarify",
            "recommend",
            "refine",
            "compare",
            "refuse"
        ]

        if intent in allowed:
            return intent

        return "recommend"

    except Exception as e:

        print(f"[Intent error] {e}")

        return "recommend"

