def is_vague_query(text: str) -> bool:
    text = text.lower().strip()

    vague_patterns = [
        "i need an assessment",
        "need an assessment",
        "suggest assessment",
        "help me choose",
        "what assessment",
        "any assessment",
        "assessment"
    ]

    if len(text.split()) <= 4:
        return True

    return any(p in text for p in vague_patterns)


